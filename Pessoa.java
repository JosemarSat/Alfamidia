    public class Pessoa {
        private String nome;
        private String telefone;
        private String dtaNasc;
        private String dtaCad;
        private String dtaAlt;

        public Pessoa() {

        }

        public String getNome() {
            return nome;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        public String getTelefone() {
            return telefone;
        }

        public void setTelefone(String telefone) {
            this.telefone = telefone;
        }

        public String getDtaNasc() {
            return dtaNasc;
        }

        public void setDtaNasc(String dtaNasc) {
            this.dtaNasc = dtaNasc;
        }

        public String getDtaCad() {
            return dtaCad;
        }

        public void setDtaCad(String dtaCad) {
            this.dtaCad = dtaCad;
        }

        public String getDtaAlt() {
            return dtaAlt;
        }

        public void setDtaAlt(String dtaAlt) {
            this.dtaAlt = dtaAlt;
        }
    }
